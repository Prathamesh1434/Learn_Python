# Mathematical operator

# Concatination operator(+) => Both argument should be tuple
t1 = (10,20,30)
print('t1 => ',t1)
t2 = (40,50,60)
print('t2 => ',t2)
t3 = t1+t2
print('t3 => ',t3)
print()

# Repeatation Opertor(*) => one argument should be tuple and other should be integer.

t1 = (10,20,30)
print('t1 => ',t1)
t2 = t1 * 3
print('t2 => ',t2)

print()

