# 1. Identity Operator
a = 10
b = 10

print(a is b)
print(a is not b)

a = 'Prathmesh'
b = 'Prathmesh'

print(a is b)
print(a is not b)

l1 = [10,20,30,40]
l2 = [10,20,30,40]
print(l1 is l2)
print(l1 is not l2)

# 2. Membership Operator
s = 'Hello , Learning Python is very easy.'
print('h' in s)
print('d' in s)
print('d' not in s)
print('Python' in s)

l = ['Sunny','Bunny','Chinny','Vinny']
print('Sunny' in l)
print('Pinny' in l)
print('Pinny' not in l)

