# 1. Escape Characters

# a. \n => new line.
print('Prathmesh Gayakwad')
print('Prathmesh\nGayakwad')

# b. \t => space b/w string.
print('PrathmeshGayakwad')
print('Prathmesh\tGayakwad')

# c. \' => Use singlecoat symbol.
print('this is \' symbol.')

# d. \" => Use dublecoat symbol.
print('this is \" symbol.')

# e. \\ => Use backslash symbol.
print('this is \\ symbol.')

# f. \b => use backspace.
print('this is\bsymbol.') #=> will remove the one char in result , before "\b".

# g. \f => form feed.
print('this is \f symbol.')

# h. \' => Carriage return.
print('this is \r symbol.')

